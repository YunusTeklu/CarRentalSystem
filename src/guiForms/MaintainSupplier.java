    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            // TODO add your handling code here:
             DefaultTableModel dtm= (DefaultTableModel) jTable1.getModel();
            int count =dtm.getRowCount();
           for(int i=count-1;i>=0;i--)
               dtm.removeRow(i);
            String sid=txtSearchSupId.getText();
            int supId=Integer.parseInt(sid);
            String supName=null, telephone=null, email=null,wereda=null,houseNo=null;
            String search="select * from supplier where supplierId="+supId+"";
            ConnectToDB ctb= new ConnectToDB();
            ResultSet rs=null;
            rs=ctb.read(search);
            while(rs.next())
            {
                supId=rs.getInt(1);
                supName=rs.getString(2);
                email=rs.getString(3);
                telephone=rs.getString(4);
                wereda=rs.getString(5);
                houseNo=rs.getString(6);
                
            }  
            dtm.addRow(new Object [] {supId, supName, telephone, email, wereda, houseNo});
            Integer suid=supId;
            txtSupId.setText(suid.toString());
            txtSupName.setText(supName);
            txtTel.setText(telephone);
            txtEmail.setText(email);
            txtWereda.setText(wereda);
            txtHouseNo.setText(houseNo);
        } catch (SQLException ex) {
            Logger.getLogger(MaintainSupplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

