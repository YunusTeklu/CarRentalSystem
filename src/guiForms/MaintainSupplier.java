    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String sid=txtSupId.getText();
        int supId=0;
        String supName=txtSupName.getText();
        String telephone=txtTel.getText();
        String email=txtEmail.getText();
        String wereda=txtWereda.getText();
        String houseNo=txtHouseNo.getText();
        String task=cmbTask.getSelectedItem().toString();
        
        String sqlInsert ,sqlUpdate,sqlDelete,searchId,operStatus= null, strSid;
        
        ConnectToDB ctb= new ConnectToDB();
        ResultSet rs= null;
        Integer spId=0;
        if(task.equalsIgnoreCase("Insert a new supplier"))
        {
            try {
                sqlInsert="INSERT INTO supplier VALUES( '"+supName+"', '"+email+"', '"+telephone+"', '"+wereda+"', '"+houseNo+"')";
                operStatus=ctb.write(sqlInsert);
                searchId="select supplierId from supplier where supplierName= '"+supName+"' ";
                rs=ctb.read(searchId);
                while(rs.next())
                {
                    spId=rs.getInt(1);
                }
                
                strSid=spId.toString();
                txtSupId.setText(strSid);
                
            } catch (SQLException ex) {
                Logger.getLogger(MaintainSupplier.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if(task.equalsIgnoreCase("Update an existing supplier"))
        {
            supId=Integer.parseInt(sid);
            sqlUpdate="update supplier "
                +"set supplierName='"+supName+"', email='"+email+"', telephone='"+telephone+"', wereda='"+wereda+"', houseNo='"+houseNo+"'"
                +"where supplierId="+supId+" ";
            operStatus=ctb.write(sqlUpdate);
        }
        else
        {
            supId=Integer.parseInt(sid);
            sqlDelete="delete from supplier where supplierId="+supId+"";
            operStatus=ctb.write(sqlDelete);
        }
         txtStatus.setText(operStatus);
    }//GEN-LAST:event_jButton2ActionPerformed

