    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String sid=txtSupId.getText();
        int supId=Integer.parseInt(sid);
        String oId=txtOrderId.getText();
        String ordStatus=cmbOrdStatus.getSelectedItem().toString();
        int orderId;
        String delDate=txtDelDate.getText();
        Date od= new Date();
        SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yy");
        String orderDate= sdf.format(od);
        String carModel=txtCarModel.getText();
        String noc=txtNoCars.getText();
        int noCars=Integer.parseInt(noc);
        String task=cmbTask.getSelectedItem().toString();
        
        String sqlInsert, sqlUpdate,searchId, stroid=null, sqlDelete,operStatus= null;
        ResultSet rs= null;
        Integer orid=0;
        ConnectToDB ctb= new ConnectToDB();
        if(task.equalsIgnoreCase("Insert a new order"))
        {
            try {
                sqlInsert="INSERT INTO supplierOrder VALUES( '"+orderDate+"','"+ordStatus+"', '"+delDate+"','"+carModel+"',"+noCars+",  "+supId+")";
                operStatus=ctb.write(sqlInsert);
                searchId="select orderId from supplierOrder where deliveryDate= '"+delDate+"'and carModel='"+carModel+"' ";
                rs=ctb.read(searchId);
                while(rs.next())
                {
                    orid=rs.getInt(1);
                }
                stroid=orid.toString();
                txtOrderId.setText(stroid);
            } catch (SQLException ex) {
                Logger.getLogger(TaskOrder.class.getName()).log(Level.SEVERE, null, ex);
            }
        }   
        else if(task.equalsIgnoreCase("Update an order"))
        {
            orderId=Integer.parseInt(oId);
            sqlUpdate="update supplierOrder "
                +"set  orderDate='"+orderDate+"' , orderStatus='"+ordStatus+"',deliveryDate='"+delDate+"',carModel='"+carModel+"', noOfCars="+noCars+", supplierID="+supId+""
                +"where orderID="+orderId+" ";
            operStatus=ctb.write(sqlUpdate);
        }    
        else
        {
            orderId=Integer.parseInt(oId);
            sqlDelete="delete from supplierOrder where orderID="+orderId+"";
            operStatus=ctb.write(sqlDelete);
        }  
         txtStatus.setText(operStatus);
    }//GEN-LAST:event_jButton4ActionPerformed

