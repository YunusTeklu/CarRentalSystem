    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            // TODO add your handling code here:
            DefaultTableModel dtm=(DefaultTableModel) jTable1.getModel();
            int count =dtm.getRowCount();
           for(int i=count-1;i>=0;i--)
               dtm.removeRow(i);
            String oId=txtSearch.getText();
            int orderId=Integer.parseInt(oId);
            String ordStatus=null,delDate=null,orderDate=null,carModel=null;
            int supId=0,noCars=0;
            String search="select * from supplierOrder where orderID="+orderId+"";
            ResultSet rs= null;
            ConnectToDB ctb=new ConnectToDB();
            rs=ctb.read(search);
            while(rs.next())
            {
                orderId=rs.getInt(1);
                ordStatus=rs.getString(2);
                orderDate=rs.getString(3);
                delDate=rs.getString(4);
                carModel=rs.getString(5);
                noCars=rs.getInt(6);
                supId=rs.getInt(7);
            }   
            dtm.addRow(new Object [] {orderId, ordStatus,orderDate, delDate, carModel, noCars, supId} );
            Integer sid=supId;
            txtSupId.setText(sid.toString());
            Integer orid=orderId;
            txtOrderId.setText(orid.toString());
            txtDelDate.setText(delDate);
            txtCarModel.setText(carModel);
            Integer nc=noCars;
            txtNoCars.setText(nc.toString());
            cmbOrdStatus.setSelectedItem(ordStatus);
        } catch (SQLException ex) {
            Logger.getLogger(TaskOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

